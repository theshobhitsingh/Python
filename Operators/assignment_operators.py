"""
Assignment Operators in Python
Used to assign values to variables, with or without operations.
"""

a = 5
print("Assignment Operators")

a += 2
print(f"a += 2 → {a}")  # 7 → Same as a = a + 2

a *= 3
print(f"a *= 3 → {a}")  # 21 → Same as a = a * 3

a //= 4
print(f"a //= 4 → {a}")  # 5 → Same as a = a // 4

a **= 2
print(f"a **= 2 → {a}")  # 25 → Same as a = a ** 2
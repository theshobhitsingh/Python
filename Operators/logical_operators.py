"""
Logical Operators in Python
"""

a = True
b = False

print("Logical Operators")
print(f"a and b  → {a and b}")   # AND → False (only True if both are True)
print(f"a or b   → {a or b}")    # OR → True (True if at least one is True)
print(f"not a    → {not a}")     # NOT → False (inverts the value of a)
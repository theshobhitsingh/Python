"""
Membership Operators in Python
Check if a value is present in a sequence (like lists, strings, tuples).
"""

list_ = [10, 20, 30]

print("Membership Operators")
print(f"20 in list_     → {20 in list_}")     # True, 20 is in the list
print(f"50 not in list_ → {50 not in list_}")  # True, 50 is NOT in the list

text = "hello"
print(f"'h' in text     → {'h' in text}")     # True
print(f"'z' not in text → {'z' not in text}")  # True